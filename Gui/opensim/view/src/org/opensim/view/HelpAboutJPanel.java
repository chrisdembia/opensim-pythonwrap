    public void paint(Graphics g) {
       super.paint(g);
        if (jTabbedPane1.getSelectedIndex()==0){
            g.drawImage( getAboutImage(), 15, 25, 380, 472, this );
        }
        else if (jTabbedPane1.getSelectedIndex()==1){
           g.drawImage( getOpenSimImage(), 15, 40, 48, 48, this );
        }
        else {
           g.drawImage( getNIHImage(), 15, 40, 48, 48, this );
           g.drawImage( getSimbiosImage(), 15, 120, 48, 22, this );
           g.drawImage( getSimTKImage(), 15, 180, 48, 28, this );
           g.drawImage( getOpenSimImage(), 15, 250, 48, 48, this );
        }
    }
    
    /** @return about image */
    private Image getAboutImage() {
        Image ret;
        if ((aboutRef == null) ||
                ((ret = (Image) aboutRef.get()) == null)) {
            ret = loadImage("/org/opensim/view/images/splash.gif");
            aboutRef = new WeakReference(ret);
        }
        return ret;
    }
