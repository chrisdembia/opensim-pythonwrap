    public void stateChanged(ChangeEvent e) {
        // Handle change in opacity slider
        int newOpacity = ((javax.swing.JSlider)e.getSource()).getValue();
        canvas.SetObjectOpacity(object, (float)newOpacity/100.0f);
        canvas.repaint();
      
        
    }
    
