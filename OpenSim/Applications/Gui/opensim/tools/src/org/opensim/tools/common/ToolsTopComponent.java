    /**
     * Obtain the ToolsTopComponent instance. Never call {@link #getDefault} directly!
     */
    public static synchronized ToolsTopComponent findInstance() {
        TopComponent win = WindowManager.getDefault().findTopComponent(PREFERRED_ID);
        if (win == null) {
            ErrorManager.getDefault().log(ErrorManager.WARNING, "Cannot find Tools component. It will not be located properly in the window system.");
            return getDefault();
        }
        if (win instanceof ToolsTopComponent) {
            return (ToolsTopComponent)win;
        }
        ErrorManager.getDefault().log(ErrorManager.WARNING, "There seem to be multiple components with the '" + PREFERRED_ID + "' ID. That is a potential source of errors and unexpected behavior.");
        return getDefault();
    }
    
